//Function to let us show or remove the menu :
function display(bool) {
  if (bool) {
      $(".container-global").show();
  } else {
      $(".container-global").hide();
  }
}

//Create all button needed :
function CreateButton() {
  for(var i in btn)
  {
    var option = document.createElement("button");
    option.classList.add("item");
    
    option.innerHTML = i;

    option.setAttribute("value", i)
    option.setAttribute("name", btn[i])

    document.querySelector(".container-global .flex-box-container").appendChild(option);
  }
}
CreateButton()


const buttons = document.querySelectorAll(".item");
buttons.forEach(element =>
{
  element.addEventListener("click", function(event)
  {
    var aName = event.target.value;
    var aRef = event.target.name;

    if (aName == element.textContent){
      $.post('http://GTA_Sound/main', JSON.stringify({
        audioName : aName,
        audioRef : aRef
      }));
    }
  })
})


//We close the menu :
const button_close = document.querySelector(".close-button")
button_close.addEventListener('click', () => {
  $.post('http://GTA_Sound/exit', JSON.stringify({}));
  display(false)
})


//Main :
window.addEventListener('message', function(event) {
  var item = event.data;
  var audioPlayer = null;

  // Check for playSound transaction
  if (event.data.transactionType == "playSound") {
  
      if (audioPlayer != null) 
      {
          audioPlayer.pause();
      }

      audioPlayer = new Howl({src: ["./sounds/" + event.data.transactionFile + ".ogg"]});
      audioPlayer.volume(event.data.transactionVolume);
      audioPlayer.play();
  }


  if (item.type === "enableui") {
      if (item.activate == true) {
          display(true);
      } else {
          display(false);
      }
  }
})